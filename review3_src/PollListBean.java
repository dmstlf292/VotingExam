package review3;

public class PollListBean {
	
	private int tnum;
	private String tquestion;
	private String tsdate;
	private String tedate;
	private String twdate;
	private int type;
	private int active;
	
	public int getTnum() {
		return tnum;
	}
	public void setTnum(int tnum) {
		this.tnum = tnum;
	}
	public String getTquestion() {
		return tquestion;
	}
	public void setTquestion(String tquestion) {
		this.tquestion = tquestion;
	}
	public String getTsdate() {
		return tsdate;
	}
	public void setTsdate(String tsdate) {
		this.tsdate = tsdate;
	}
	public String getTedate() {
		return tedate;
	}
	public void setTedate(String tedate) {
		this.tedate = tedate;
	}
	public String getTwdate() {
		return twdate;
	}
	public void setTwdate(String twdate) {
		this.twdate = twdate;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getActive() {
		return active;
	}
	public void setActive(int active) {
		this.active = active;
	}
	
}
